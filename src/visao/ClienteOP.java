    private void jAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAdicionarActionPerformed
       String nome = jNome.getText();
       String endereco = jEndereco.getText();
       String fone = jFone.getText();
        Cliente a = new Cliente(nome, endereco, fone);
        ClienteDAO am = new ClienteDAO();
        
        jNome.setText("");
        jEndereco.setText("");
        jFone.setText("");
        
        am.inserir(a);
        readT(); 
    }//GEN-LAST:event_jAdicionarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        jNome.setText(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 1)));
        jEndereco.setText(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 2)));
        jFone.setText(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 3)));
    }//GEN-LAST:event_jTable1MouseClicked

    private void jAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAtualizarActionPerformed
        Cliente a = new Cliente();
        ClienteDAO am = new ClienteDAO();
        Object id = jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        String i = String.valueOf(id);
        a.setIdCliente(Integer.parseInt(i));
        a.setNome(jNome.getText());
        a.setEndereco(jEndereco.getText());
        a.setFone(jFone.getText());

        try {
            am.atualizar(a);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: Atualizar banco de dados!");
        }
        readT();
    }//GEN-LAST:event_jAtualizarActionPerformed

    private void jRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRemoverActionPerformed
       Cliente a = new Cliente();
        ClienteDAO am = new ClienteDAO();
        
        Object id = jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        String i = String.valueOf(id);
        a.setIdCliente(Integer.parseInt(i));
        a.setNome(jNome.getText());
        a.setEndereco(jEndereco.getText());
        a.setFone(jFone.getText());

        try {
            am.deletar(a.getNome());
            jTable1.remove(jTable1.getSelectedRow());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: Ao remover Amigo!");
        }
        readT();
    }//GEN-LAST:event_jRemoverActionPerformed

